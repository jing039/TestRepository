package first_java;

public class JavaOperators {

	public static void main(String[] args) {
		/** There are 5 types of Java Operators
		 * 
		 * Arithmetic Operators
		 * Assignment Operators
		 * Comparison/Relational Operators
		 * Logical Operators
		 * Bitwise Operators
		 * 
		 */
		
		
		//Learning Arithmetic Operators
		//+, -, *, /, %, ++, --
		int a = 10;
		int b = 5;
		int result;
		
		//Addition
		result = a+b;
		System.out.println("Addition result is " + result);
		//Subtraction
		result = a-b;		
		System.out.println("Subtraction result is " + result);
		//Multiplication
		result = a*b;
		System.out.println("Multiplication result is " + result);
		//Division
		result = a/b;
		System.out.println("Division result is " + result);
		//Modulus
		result = a%+b;
		System.out.println("Modulus result is " + result);
		//Incremental: does not work in this way
		result = a+b;
		System.out.println("Incremental result is " + a++);
		//Decremental: does not work in this way
		result = a+b;
		System.out.println("Decremental result is " + a--);
		
		
		
		
		
		
		

	}

}
