package first_java;

public class ForLoop_Demo {

	public static void main(String[] args) {

		/** For loop syntax
		 * 
		 * for (initialization; how far/end point;  increment/decrement){
		 * code to be executed
		 * }
		 * 
		 */

		// Write "I love you" 10 times using for loop.
		for(int i = 1; i <= 10; i++) {
			System.out.println("I love you.");
		}
		System.out.println("The End.");
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
